using System;
using System.Threading;
using System.Threading.Tasks;
using MiKiNuo.Mvi.Application.MVI.IntentHandler;

namespace MiKiNuo.Mvi.Samples.Avalonia.Features.Dashboard.Outpatient.ClinicalEditor;

/// <summary>
/// 表示门诊病历编辑意图处理器。
/// </summary>
public sealed class ClinicalEditorIntentHandler
    : IMviIntentHandler<ClinicalEditorState, ClinicalEditorIntent, ClinicalEditorEffect>
{
    /// <summary>
    /// 处理意图并产生动作副作用。
    /// </summary>
    /// <param name="state">当前状态。</param>
    /// <param name="intent">用户意图。</param>
    /// <param name="cancellationToken">取消标记。</param>
    /// <returns>动作副作用集合。</returns>
    public ValueTask<IReadOnlyList<ClinicalEditorEffect>> HandleAsync(
        ClinicalEditorState state,
        ClinicalEditorIntent intent,
        CancellationToken cancellationToken = default)
    {
        ArgumentNullException.ThrowIfNull(state);
        ArgumentNullException.ThrowIfNull(intent);
        return new ValueTask<IReadOnlyList<ClinicalEditorEffect>>(Array.Empty<ClinicalEditorEffect>());
    }
}
