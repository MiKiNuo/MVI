    private async ValueTask<DashboardNavigationResponse> NavigateDashboardPageAsync(
        string pageKey,
        CancellationToken cancellationToken)
    {
        if (_dashboardStore is null)
        {
            return new DashboardNavigationResponse(pageKey, false);
        }

        (string title, string description, object pageViewModel) = ResolveDashboardPage(pageKey);
        await _dashboardStore.DispatchAsync(
            new DashboardIntent.ShowPage(title, description, pageViewModel),
            cancellationToken).ConfigureAwait(false);

        if (_headerStore is not null)
        {
            await _headerStore.DispatchAsync(
                new HeaderIntent.UpdateTitle($"HIS/EMR 综合工作台 · {title}", description),
                cancellationToken).ConfigureAwait(false);
        }

        return new DashboardNavigationResponse(title, true);
    }

    private async ValueTask<PatientEncounterResponse> OpenPatientEncounterAsync(
        string patientName,
        CancellationToken cancellationToken)
    {
        if (_clinicalEditorStore is not null)
        {
            await _clinicalEditorStore.DispatchAsync(
                new ClinicalEditorIntent.LoadPatient(patientName),
                cancellationToken).ConfigureAwait(false);
        }

        if (_clinicalReminderStore is not null)
        {
            await _clinicalReminderStore.DispatchAsync(
                new ClinicalReminderIntent.LoadPatient(patientName),
                cancellationToken).ConfigureAwait(false);
        }

        return new PatientEncounterResponse(patientName, true);
    }

    private async ValueTask<DashboardComponentInteractionResponse> CoordinateDashboardComponentAsync(
        DashboardComponentInteractionRequest request,
        CancellationToken cancellationToken)
    {
        string message = CreateInteractionMessage(request);
        if (_auditTimelineStores.TryGetValue(request.PageKey, out IMviStore<AuditTimelineState, AuditTimelineIntent, AuditTimelineEffect>? auditStore))
        {
            await auditStore.DispatchAsync(
                new AuditTimelineIntent.AppendEntry($"Mediator 协调：{message}"),
                cancellationToken).ConfigureAwait(false);
        }

        if (_businessPageStores.TryGetValue(request.PageKey, out IMviStore<BusinessCompositePageState, BusinessCompositePageIntent, BusinessCompositePageEffect>? pageStore))
        {
            await pageStore.DispatchAsync(
                new BusinessCompositePageIntent.UpdateContext(request.ContextText, message),
                cancellationToken).ConfigureAwait(false);
            await pageStore.DispatchAsync(
                new BusinessCompositePageIntent.AppendInteractionLog($"{DateTime.Now:HH:mm:ss} · {message}"),
                cancellationToken).ConfigureAwait(false);
        }

        if (string.Equals(request.PageKey, "架构验证中心", StringComparison.Ordinal) && _architectureValidationStore is not null)
        {
            await _architectureValidationStore.DispatchAsync(
                new ArchitectureValidationIntent.UpdateContext(request.ContextText, message),
                cancellationToken).ConfigureAwait(false);
            await _architectureValidationStore.DispatchAsync(
                new ArchitectureValidationIntent.AppendInteractionLog($"{DateTime.Now:HH:mm:ss} · {message}"),
                cancellationToken).ConfigureAwait(false);
        }

        if (_pageComponentKeys.TryGetValue(request.PageKey, out IReadOnlyList<string>? componentKeys))
        {
            foreach (string componentKey in componentKeys)
            {
                if (string.Equals(componentKey, request.SourceComponent, StringComparison.Ordinal))
                {
                    continue;
                }

                if (_componentUpdateHandlers.TryGetValue(componentKey, out Func<string, CancellationToken, ValueTask>? handler))
                {
                    await handler(message, cancellationToken).ConfigureAwait(false);
                }
            }
        }

        return new DashboardComponentInteractionResponse(message, true);
    }

