    /// <inheritdoc />
    public void Record(MviDiagnosticEntry entry)
    {
        ArgumentNullException.ThrowIfNull(entry);

        string message = entry.ToDisplayText();
        _diagnosticBacklog.Add(message);

        foreach (IMviStore<AuditTimelineState, AuditTimelineIntent, AuditTimelineEffect> auditStore in _auditTimelineStores.Values)
        {
            _ = auditStore.DispatchAsync(new AuditTimelineIntent.AppendEntry(message));
        }
    }

    /// <inheritdoc />
    public async ValueTask NavigateToDashboardAsync(string displayName, CancellationToken cancellationToken = default)
    {
        DashboardViewModel dashboardViewModel = CreateDashboardViewModel(displayName);
        await _shellViewModel.ShowPageAsync("HIS/EMR 组合式 Dashboard", dashboardViewModel).ConfigureAwait(false);
    }

    /// <inheritdoc />
    public async ValueTask<TResponse> SendAsync<TRequest, TResponse>(
        TRequest request,
        CancellationToken cancellationToken = default)
        where TRequest : notnull
    {
        ArgumentNullException.ThrowIfNull(request);

        if (request is NavigateDashboardPageRequest navigationRequest
            && typeof(TResponse) == typeof(DashboardNavigationResponse))
        {
            DashboardNavigationResponse response = await NavigateDashboardPageAsync(
                navigationRequest.PageKey,
                cancellationToken).ConfigureAwait(false);
            return (TResponse)(object)response;
        }

        if (request is OpenPatientEncounterRequest openPatientRequest
            && typeof(TResponse) == typeof(PatientEncounterResponse))
        {
            PatientEncounterResponse response = await OpenPatientEncounterAsync(
                openPatientRequest.PatientName,
                cancellationToken).ConfigureAwait(false);
            return (TResponse)(object)response;
        }

        if (request is DashboardComponentInteractionRequest interactionRequest
            && typeof(TResponse) == typeof(DashboardComponentInteractionResponse))
        {
            DashboardComponentInteractionResponse response = await CoordinateDashboardComponentAsync(
                interactionRequest,
                cancellationToken).ConfigureAwait(false);
            return (TResponse)(object)response;
        }

        throw new InvalidOperationException($"未注册中介者路由：{typeof(TRequest).FullName} -> {typeof(TResponse).FullName}");
    }

    private object ResolveCore(Type serviceType)
    {
        ArgumentNullException.ThrowIfNull(serviceType);

        if (serviceType == typeof(IAuthService))
        {
            return _authService;
        }

        if (serviceType == typeof(ILoginNavigationService))
        {
            return this;
        }

        if (serviceType == typeof(IMviMediator))
        {
            return this;
        }

        if (serviceType == typeof(IMviDiagnosticSink))
        {
            return this;
        }

        if (serviceType == typeof(AppShellViewModel))
        {
            return _shellViewModel;
        }

        if (serviceType == typeof(LoginViewModel))
        {
            return _loginViewModel ??= new LoginViewModel(ResolveLoginStore());
        }

        if (serviceType == typeof(IMviViewRegistry))
        {
            return _viewRegistry ??= new SampleGeneratedViewRegistry();
        }

        if (serviceType == typeof(IMviMediator))
        {
            return this;
        }

        throw new InvalidOperationException($"未注册服务：{serviceType.FullName}");
    }

    internal IReadOnlyList<IMviMiddleware<TState, TIntent, TEffect>> CreateDefaultMiddlewares<TState, TIntent, TEffect>(string componentName)
        where TState : MiKiNuo.Mvi.Domain.MVI.State.IMviState
        where TIntent : MiKiNuo.Mvi.Domain.MVI.Intent.IMviIntent
        where TEffect : MiKiNuo.Mvi.Domain.MVI.Effect.IMviEffect
    {
        return new IMviMiddleware<TState, TIntent, TEffect>[]
        {
            new MviLoggingMiddleware<TState, TIntent, TEffect>(this, componentName),
            new MviPerformanceMiddleware<TState, TIntent, TEffect>(this, componentName)
        };
    }

